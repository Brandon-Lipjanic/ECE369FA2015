	lw   $t3, 0($s6)
	lw   $t4, 0($s7)
	sub $t9, $t3, $t4		# t9 = fACM(val) - wACM(val)
	slt $t8, $t9, $zero		# t8 = 1 if t9 is negative
	beq $t8, $zero, positive	# go to positive if t9 is pos else do 2's compliment
	nor $t9, $t9, $zero		# invert t4 store in t9
	addi $t9, $t9, 1		# t9 = t9 +1

