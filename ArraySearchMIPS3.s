rowLoop:

	slt  $t9, $t0, $s3		# if (row < windowCol) == 1
	beq  $t9, $zero, compareReturn  # 1 != 0, don't branch	

	move $s6, $t6			# fACM = fARM
	move $s7, $t7			# wACM = wARM
	

	
